-- dataset: ec-perf-v1
-- purpose: 性能学習用のECデータセット（publicスキーマ）
-- 方針:
--   - ec-v1 と同系統のテーブル構成
--   - 性能学習用なので、まずは最小限の索引（PK/UNIQUE）だけ
--   - Phase 12 のSQLで索引を追加/削除して差分を体験する
-- 念のため作り直し（Docker初期化時は空DBだが、再利用時にも安全）
DROP TABLE IF EXISTS public.order_item CASCADE;

DROP TABLE IF EXISTS public.customer_order CASCADE;

DROP TABLE IF EXISTS public.user_address CASCADE;

DROP TABLE IF EXISTS public.product CASCADE;

DROP TABLE IF EXISTS public.app_user CASCADE;

-- 1) ユーザー
CREATE TABLE public.app_user (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email TEXT NOT NULL UNIQUE,
    full_name TEXT NOT NULL,
    user_tier TEXT NOT NULL CHECK (user_tier IN ('free', 'standard', 'premium')),
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
);

COMMENT ON TABLE public.app_user IS '性能学習用: ユーザー';

-- 2) 住所（デフォルト住所を1件持つ想定）
CREATE TABLE public.user_address (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL REFERENCES public.app_user (id) ON DELETE CASCADE,
    prefecture TEXT NOT NULL,
    city TEXT NOT NULL,
    is_default BOOLEAN NOT NULL DEFAULT FALSE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
);

COMMENT ON TABLE public.user_address IS '性能学習用: ユーザー住所（都道府県レポート用）';

-- 3) 商品
CREATE TABLE public.product (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    sku TEXT NOT NULL UNIQUE,
    name TEXT NOT NULL,
    category TEXT NOT NULL,
    price_yen BIGINT NOT NULL CHECK (price_yen > 0),
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
);

COMMENT ON TABLE public.product IS '性能学習用: 商品';

-- 4) 注文
CREATE TABLE public.customer_order (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL REFERENCES public.app_user (id),
    order_status TEXT NOT NULL CHECK (
        order_status IN (
            'placed',
            'paid',
            'shipped',
            'delivered',
            'cancelled'
        )
    ),
    ordered_at TIMESTAMPTZ NOT NULL,
    payment_method TEXT NOT NULL CHECK (
        payment_method IN ('card', 'bank', 'cod', 'wallet')
    ),
    channel TEXT NOT NULL CHECK (channel IN ('web', 'ios', 'android')),
    coupon_code TEXT
);

COMMENT ON TABLE public.customer_order IS '性能学習用: 注文';

-- 5) 注文明細
CREATE TABLE public.order_item (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_id BIGINT NOT NULL REFERENCES public.customer_order (id) ON DELETE CASCADE,
    product_id BIGINT NOT NULL REFERENCES public.product (id),
    quantity INTEGER NOT NULL CHECK (quantity > 0),
    unit_price_yen BIGINT NOT NULL CHECK (unit_price_yen > 0)
);

COMMENT ON TABLE public.order_item IS '性能学習用: 注文明細';

-- 6) 補足（性能学習のため、FK列の索引はあえて未作成）
SELECT
    'ec-perf-v1 schema created' AS message;
